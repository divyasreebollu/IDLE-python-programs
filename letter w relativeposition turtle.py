import turtle

# create a turtle object
t = turtle.Turtle()

# set the initial position of the turtle
t.penup()
t.goto(-100, 0)
t.pendown()

# draw the letter w using relative movements
t.right(90)
t.forward(100)
t.left(145)
t.forward(50)
t.right(105)
t.forward(55)
t.left(140)
t.forward(100)
